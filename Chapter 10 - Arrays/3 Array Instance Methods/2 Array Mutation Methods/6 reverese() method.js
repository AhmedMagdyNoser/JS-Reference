let A = ['Ahmed', 'Magdy', 'Ibrahim'];

// The reverse() reverses the order of the elements in an array and returns a reference to the same array.

// To reverse the array
A.reverse(); // ['Ibrahim', 'Magdy', 'Ahmed']

A = ['Ahmed', 'Magdy', 'Ibrahim'];

// To reverse the array from a specific index to a specific index (without the end)
A.reverse(0, 2); // ['Magdy', 'Ahmed', 'Ibrahim']
