// while loops

/*

while Loops Syntax:

while loops are used to execute a block of code as long as a specified condition is true.

while (condition) {
  code block to be executed
}

*/

// ===========================================================================================

// Simple Example:

// The while loop below loops through the numbers 0 to 9.

let i = 0;

while (i < 10) {
  console.log(i);
  i++;
}

// in a while loop, the initialization and incrementation is done manually.
