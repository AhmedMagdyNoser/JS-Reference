// First Class Functions

// JavaScript has first-class functions.

// This means functions are treated like any other variable.
// 1. A function can be assigned as a value to a variable.
// 2. A function can be passed as an argument to other functions.
// 3. A function can be returned by another function.

// Note:
// A function that returns a function or takes other functions as arguments is called a higher-order function.
