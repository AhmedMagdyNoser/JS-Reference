// Template Literals (String Interpolation)

// Use the backtick character (`) to interpolate variables into strings
const name = 'Ahmed Magdy Ibrahim';
const job = 'Software Engineer';
console.log(`${name} is a ${job}`); // 'Ahmed Magdy Ibrahim is a Software Engineer'

// Template literals can span multiple lines
console.log(`Ahmed Magdy Ibrahim
Software Engineer`);

// Template literals can contain escape characters
console.log(`Ahmed Magdy Ibrahim\nSoftware Engineer`);
