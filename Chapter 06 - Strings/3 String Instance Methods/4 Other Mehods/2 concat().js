// concat()

const S = 'Ahmed Magdy Ibrahim';

// The concat() takes one or more arguments: the strings to concatenate
console.log(S.concat(' is a software engineer')); // 'Ahmed Magdy Ibrahim is a software engineer'
console.log(S.concat(' is a software engineer', ' at Google')); // 'Ahmed Magdy Ibrahim is a software engineer at Google'

// Another way to concatenate strings is by using the + operator
console.log(S + ' is a software engineer'); // 'Ahmed Magdy Ibrahim is a software engineer'
console.log(S + ' is a software engineer' + ' at Google'); // 'Ahmed Magdy Ibrahim is a software engineer at Google'
